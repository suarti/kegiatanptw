    <h2>Framework dan Jenis Framework</h2>
    <h4>1. Apa Itu Framework ??</h4>
    <p>Framework adalah kerangka kerja yang digunakan untuk mengembangkan aplikasi berbasis desktop atau aplikasi berbasis website. Dengan menggunakan framework Anda akan lebih mudah untuk membuat aplikasi atau website. Itu karena Anda hanya perlu menyusun komponen-komponen pemrograman yang sudah jadi. Dengan kata lain, Anda tidak perlu membuat berbagai fitur dari awal lagi</p>
    <p>Ada dua jenis Framework dalam dunia pemrograman yaitu Desktop Framework dan Web Framework, keduanya
        memiliki fungsi yang berbeda, Desktop Framewok digunakan untuk membangun aplikasi berbasis desktop
        sedangkan Web Framewok digunakan untuk membangun aplikasi berbasis web.</p>
    <h4>2. Fungsi Framework</h4>
    <p>1. Program Menjadi Lebih Terstruktur dan Tersusun</p>
    <p>2. Praktis untuk Developer</p>
    <p>3. Memiliki Keamanan yang Lebih Unggul</p>

